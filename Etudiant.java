package gestion;

public class Etudiant {
	
	String nom;
    String prenom;
    String adresse;
    String email;
    String telephone;
    double note;

    public Etudiant(String nom, String prenom, String adresse, String email, String telephone, double note) {
                    
        this.nom = nom;
        this.prenom = prenom;
        this.adresse = adresse;
        this.email = email;
        this.telephone = telephone;
        this.note = note;
    }


}
